#include <stdlib.h>

/**
 * add - add 2 int
 * @a: first int
 * @b: ssecond int
 * Return: add
 */

int add(int a, int b)
{

	return (a + b);
}

/**
 * sub - sub 2 int
 * @a: first int
 * @b: ssecond int
 * Return: sub
 */

int sub(int a, int b)
{

        return (a - b);
}

/**
 * mul - mul 2 int
 * @a: first int
 * @b: ssecond int
 * Return: mul
 */

int mul(int a, int b)
{

        return (a * b);
}

/**
 * mod - mod 2 int
 * @a: first int
 * @b: ssecond int
 * Return: mod
 */

int mod(int a, int b)
{

        return (a % b);
}
